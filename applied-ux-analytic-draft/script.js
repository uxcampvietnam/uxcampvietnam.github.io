function buildAsciiHTML(text) {

  return text
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')

    .split('\n')

    .map(line => {

      return `
                <div class="ascii-row">
                    ${line
          .split('')
          .map(char => {

            if (char === ' ') {
              return `<span class="ascii-char space">&nbsp;</span>`;
            }

            return `<span class="ascii-char">${char}</span>`;

          })
          .join('')
        }
                </div>
            `;

    })

    .join('');
}

